def points(games):
  result = 0
  
  for i in games:
    if i[0] > i[2]:
      result += 3
    elif i[0] == i[2]:
      result += 1
          
  return result

print(points(['1:0','2:0','3:0','4:0','2:1','3:1','4:1','3:2','4:2','4:3']))
# 30

print(points(['1:1','2:2','3:3','4:4','2:2','3:3','4:4','3:3','4:4','4:4']))
# 10

print(points(['0:1','0:2','0:3','0:4','1:2','1:3','1:4','2:3','2:4','3:4']))
# 0
